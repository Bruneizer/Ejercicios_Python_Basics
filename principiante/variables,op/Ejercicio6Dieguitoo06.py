# 6 Operaciones matemáticas:
# Solicita dos números y realiza operaciones matemáticas básicas (suma, resta, multiplicación, división).

Numeros = int(input("Ingresa un numero: "))
Numeros1 = int(input("Ingresa un numero: "))

suma = Numeros + Numeros1
resta = Numeros - Numeros1
multiplicacion = Numeros * Numeros1
division = Numeros / Numeros1

print(f"El resultado de la suma es {suma}")
print(f"El resultado de la resta es {resta}")
print(f"El resultado de la multiplicacion es {multiplicacion}")
print(f"El resultado de la division es {division}")